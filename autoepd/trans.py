def trans(file_in, file_out, dict_raw):
	get_list = []
	sheetindex = 0
	workbook = xlrd.open_workbook(file_in)
	sheet = workbook.sheet_by_index(sheetindex) 
	table_row = pd.read_excel(file_in, sheetname=sheet.name, index_col=[0])
	for col in table_row['材质']:
		try:
			get_list.append(dict_raw[col])
		except:
			get_list.append("未知材料，待定！")
	table_row['材质归类'] = get_list
	writer = pd.ExcelWriter(file_out)
	table_row.to_excel(writer,'材料转换')
	writer.save()

